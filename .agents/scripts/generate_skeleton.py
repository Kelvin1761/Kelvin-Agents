#!/usr/bin/env python3
"""
generate_skeleton.py — V2 Analysis Skeleton Auto-Generator
===========================================================

Reads a Facts.md file (generated by inject_fact_anchors.py or
inject_hkjc_fact_anchors.py) and produces an Analysis.md skeleton with:
  - 📋 完整賽績檔案 tables pre-embedded (from Facts.md)
  - 📊 段速趨勢 + ⚡ EEM 能量摘要 pre-embedded
  - All analysis fields as [FILL] placeholders for the LLM
  - ★ V2: Pre-generated Final Verdict table (Part 3) to prevent drift

The LLM only needs to fill in the analytical sections, not copy data.

Usage:
    python3 generate_skeleton.py <Facts.md> [--output <Analysis.md>] [--batch-size 3]
        [--mode au|hkjc]   # auto-detected if not specified
"""
import re
import sys
from pathlib import Path


# ── Facts.md Parsers ─────────────────────────────────────────────────────

def detect_mode(text: str) -> str:
    """Auto-detect whether this is AU or HKJC format."""
    if '### 馬匹 #' in text:
        return 'au'
    elif '### 馬號' in text:
        return 'hkjc'
    return 'au'  # default


def parse_facts_md_au(text: str) -> list[dict]:
    """Parse AU-format Facts.md and extract per-horse blocks."""
    horse_pattern = re.compile(r'^### 馬匹 #(\d+) (.+?) \(檔位 (\d+)\)', re.MULTILINE)
    matches = list(horse_pattern.finditer(text))

    horses = []
    for i, match in enumerate(matches):
        num = int(match.group(1))
        name = match.group(2).strip()
        barrier = int(match.group(3))

        start = match.start()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        block = text[start:end].strip()

        anchor = _extract_between(block, '📌 事實錨點', '📋 完整賽績檔案')
        table = _extract_between(block, '📋 完整賽績檔案', '📊 段速趨勢')
        trends = _extract_between(block, '📊 段速趨勢', '⚡ EEM 能量摘要')
        eem = _extract_between(block, '⚡ EEM 能量摘要', '🔗 賽績線')
        formline = _extract_between(block, '🔗 賽績線', '🔧 引擎與距離')
        engine = _extract_from(block, '🔧 引擎與距離')

        horses.append({
            'num': num, 'name': name, 'barrier': barrier,
            'jockey': '', 'trainer': '', 'weight': 0,
            'anchor': anchor, 'table': table, 'trends': trends,
            'eem': eem, 'formline': formline, 'engine': engine,
            'mode': 'au',
        })

    return horses


def parse_facts_md_hkjc(text: str) -> list[dict]:
    """Parse HKJC-format Facts.md and extract per-horse blocks."""
    # Pattern: ### 馬號 1 — 智勝勳章 | 騎師: 奧爾民 | 練馬師: 方嘉柏 | 負磅: 134 | 檔位: 7
    horse_pattern = re.compile(
        r'^### 馬號 (\d+) — (.+?)(?:\s*\|)', re.MULTILINE
    )
    matches = list(horse_pattern.finditer(text))

    horses = []
    for i, match in enumerate(matches):
        num = int(match.group(1))
        name = match.group(2).strip()

        start = match.start()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        block = text[start:end].strip()

        # Extract header details
        header_line = block.split('\n')[0]
        jockey_m = re.search(r'騎師:\s*(\S+)', header_line)
        trainer_m = re.search(r'練馬師:\s*(\S+)', header_line)
        weight_m = re.search(r'負磅:\s*(\d+)', header_line)
        barrier_m = re.search(r'檔位:\s*(\d+)', header_line)

        jockey = jockey_m.group(1) if jockey_m else ''
        trainer = trainer_m.group(1) if trainer_m else ''
        weight = int(weight_m.group(1)) if weight_m else 0
        barrier = int(barrier_m.group(1)) if barrier_m else 0

        # Extract data blocks using different markers for HKJC format
        summary = _extract_between(block, '📌 **賽績總結', '📋 **完整賽績檔案')
        table = _extract_between(block, '📋 **完整賽績檔案', '📊 **段速趨勢')
        if '📋 **較舊歷史賽績' in table:
            table = table[:table.find('📋 **較舊歷史賽績')].strip()
        if not table:
            table = _extract_between(block, '📋 **完整賽績檔案', '🔧 **引擎距離')
            if '📋 **較舊歷史賽績' in table:
                table = table[:table.find('📋 **較舊歷史賽績')].strip()
        
        trends = _extract_between(block, '📊 **段速趨勢', '🔧 **引擎距離')
        engine = _extract_between(block, '🔧 **引擎距離', '📏 **頭馬距離')
        if not engine:
            engine = _extract_between(block, '🔧 **引擎距離', '💡 **LLM')
        
        # New dimensions
        margin_trend = _extract_between(block, '📏 **頭馬距離', '📊 **體重趨勢')
        weight_trend = _extract_between(block, '📊 **體重趨勢', '🔧 **配備變動')
        gear = _extract_between(block, '🔧 **配備變動', '📈 **評分變動')
        rating = _extract_between(block, '📈 **評分變動', '🏃 **走位 PI')
        pi = _extract_between(block, '🏃 **走位 PI', '🔗 **賽績線')
        if not pi:
            pi = _extract_between(block, '🏃 **走位 PI', '💡 **LLM')
        formline = _extract_between(block, '🔗 **賽績線', '💡 **LLM')

        # Combine new dimensions into one block
        new_dims = '\n'.join(filter(None, [margin_trend, weight_trend, gear, rating, pi]))

        horses.append({
            'num': num, 'name': name, 'barrier': barrier,
            'jockey': jockey, 'trainer': trainer, 'weight': weight,
            'summary': summary, 'table': table, 'trends': trends,
            'engine': engine, 'new_dims': new_dims, 'formline': formline,
            'mode': 'hkjc',
        })

    return horses


def _extract_between(block: str, start_marker: str, end_marker: str) -> str:
    """Extract text from line containing start_marker to line before end_marker."""
    start_idx = block.find(start_marker)
    if start_idx == -1:
        return ''
    line_start = block.rfind('\n', 0, start_idx)
    line_start = line_start + 1 if line_start != -1 else 0

    end_idx = block.find(end_marker, start_idx + len(start_marker))
    if end_idx == -1:
        return block[line_start:].strip()
    line_end = block.rfind('\n', 0, end_idx)
    if line_end <= line_start:
        return block[line_start:end_idx].strip()
    return block[line_start:line_end].strip()


def _extract_from(block: str, start_marker: str) -> str:
    """Extract text from line containing start_marker to end of block."""
    start_idx = block.find(start_marker)
    if start_idx == -1:
        return ''
    line_start = block.rfind('\n', 0, start_idx)
    line_start = line_start + 1 if line_start != -1 else 0
    return block[line_start:].strip()


