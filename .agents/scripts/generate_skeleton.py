# ── Main ─────────────────────────────────────────────────────────────────

def main():
    import argparse as _ap
    parser = _ap.ArgumentParser(
        description='generate_skeleton.py V2 — 自動骨架生成器（從 Facts.md 嵌入數據 + Final Verdict 鎖定）'
    )
    parser.add_argument('facts', help='Facts.md 路徑')
    parser.add_argument('--output', '-o', default=None,
                        help='輸出 Analysis.md 路徑（默認 = 同目錄）')
    parser.add_argument('--batch-size', type=int, default=3,
                        help='每批馬匹數（默認 3）')
    parser.add_argument('--mode', choices=['au', 'hkjc'], default=None,
                        help='模式：au 或 hkjc（自動偵測）')
    args = parser.parse_args()

    facts_path = args.facts
    if not Path(facts_path).exists():
        print(f"❌ 找不到文件: {facts_path}")
        sys.exit(1)

    text = Path(facts_path).read_text(encoding='utf-8')
    mode = args.mode or detect_mode(text)

    if mode == 'hkjc':
        horses = parse_facts_md_hkjc(text)
    else:
        horses = parse_facts_md_au(text)

    if not horses:
        print("❌ Facts.md 中找不到馬匹")
        sys.exit(1)

    print(f"📝 模式: {mode.upper()} | 馬匹: {len(horses)}")

    # Generate skeleton
    body_lines = []

    # Part 1 placeholder
    body_lines.append('## [第一部分] 🗺️ 戰場全景')
    body_lines.append('')
    body_lines.append('[FILL — 由 LLM 填充]')
    body_lines.append('')
    body_lines.append('---')
    body_lines.append('')
    body_lines.append('## [第二部分] 🔬 深度顯微鏡')
    body_lines.append('')

    # Generate per-horse skeletons
    batch_size = args.batch_size
    skeleton_fn = generate_horse_skeleton_hkjc if mode == 'hkjc' else generate_horse_skeleton_au

    for i, horse in enumerate(horses):
        body_lines.append(skeleton_fn(horse))
        body_lines.append('---')
        body_lines.append('')

        # Batch compliance check
        batch_pos = (i % batch_size) + 1
        if batch_pos == batch_size or i == len(horses) - 1:
            done = i + 1
            body_lines.append(
                f'✅ 批次完成:{done}/{len(horses)} 馬匹 | '
                f'每匹含 ⏱️近績+📋賽績檔案+🐴剖析+🔗賽績線+🧭陣型+⚠️風險+📊矩陣+💡核心邏輯+⭐評級 全 9 欄位 | D 級馬 ≥300 字 ✔️'
            )
            body_lines.append('')

    # Part 3-5: Pre-generated Final Verdict (drift-proof)
    body_lines.append('---')
