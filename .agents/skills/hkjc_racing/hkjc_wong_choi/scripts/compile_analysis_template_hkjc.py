def _write_compile_blocked(output_path: str, message: str) -> None:
    out = Path(output_path)
    runtime_dir = out.parent / ".runtime"
    runtime_dir.mkdir(parents=True, exist_ok=True)
    names = {f"compile_blocked_{out.stem}.txt"}
    race_match = re.search(r"Race[_ ](\d+)", out.name)
    if race_match:
        names.add(f"compile_blocked_Race_{race_match.group(1)}.txt")
    for name in names:
        (runtime_dir / name).write_text(message, encoding="utf-8")

def _score_to_tick(score):
    """Convert a score to 5-tier tick: ✅✅/✅/➖/❌/❌❌."""
    if isinstance(score, str):
        s = score.strip()
        if s == '✅✅': return '✅✅'
        if '✅' in s: return '✅'
        if s == '❌❌': return '❌❌'
        if '❌' in s: return '❌'
        if '➖' in s or 'N/A' in s: return '➖'
        try:
            score = int(s)
        except ValueError:
            return '➖'
    if not isinstance(score, (int, float)):
        return '➖'
    if score >= 9:
        return '✅✅'
    elif score >= 7:
        return '✅'
    elif score >= 4:
        return '➖'
    elif score >= 2:
        return '❌'
    else:
        return '❌❌'

def _count_dimensions(dims: dict) -> dict:
    """Count dimensions with 5-tier display. ✅✅/❌❌ count as one pass/fail."""
    counts = {
        'core_strong': 0, 'core_neutral': 0, 'core_weak': 0,
        'semi_strong': 0, 'semi_neutral': 0, 'semi_weak': 0,
        'aux_strong': 0, 'aux_neutral': 0, 'aux_weak': 0,
        'total_strong': 0, 'total_weak': 0,
        'has_core_weak': False, 'has_core_double_weak': False,
        # Raw counts for display
        'core_dbl_strong': 0, 'semi_dbl_strong': 0, 'aux_dbl_strong': 0,
        'total_dbl_strong': 0, 'total_dbl_weak': 0,
        'core_single_strong': 0, 'semi_single_strong': 0, 'aux_single_strong': 0,
    }
    for dim_key, value in dims.items():
        v = value.strip() if isinstance(value, str) else str(value)
        if v in ('N/A', '不計入'):
            continue
        dim_type = DIMENSION_TYPES.get(dim_key, 'auxiliary')
        if v == '✅✅':
            counts['total_strong'] += 1
            counts['total_dbl_strong'] += 1
            if dim_type == 'core':
                counts['core_strong'] += 1
                counts['core_dbl_strong'] += 1
            elif dim_type == 'semi_core':
                counts['semi_strong'] += 1
                counts['semi_dbl_strong'] += 1
            else:
                counts['aux_strong'] += 1
                counts['aux_dbl_strong'] += 1
        elif v == '✅':
            counts['total_strong'] += 1
            if dim_type == 'core':
                counts['core_strong'] += 1
                counts['core_single_strong'] += 1
            elif dim_type == 'semi_core':
                counts['semi_strong'] += 1
                counts['semi_single_strong'] += 1
            else:
                counts['aux_strong'] += 1
                counts['aux_single_strong'] += 1
        elif v == '❌❌':
            counts['total_weak'] += 1
            counts['total_dbl_weak'] += 1
            if dim_type == 'core':
                counts['core_weak'] += 1
                counts['has_core_weak'] = True
                counts['has_core_double_weak'] = True
            elif dim_type == 'semi_core': counts['semi_weak'] += 1
            else: counts['aux_weak'] += 1
        elif v == '❌':
            counts['total_weak'] += 1
            if dim_type == 'core':
                counts['core_weak'] += 1
                counts['has_core_weak'] = True
            elif dim_type == 'semi_core': counts['semi_weak'] += 1
            else: counts['aux_weak'] += 1
        else:  # ➖ neutral
            if dim_type == 'core': counts['core_neutral'] += 1
            elif dim_type == 'semi_core': counts['semi_neutral'] += 1
            else: counts['aux_neutral'] += 1
    return counts

def _lookup_base_grade(c: dict) -> tuple:
    cs, ss, axs, tw = c['core_strong'], c['semi_strong'], c['aux_strong'], c['total_weak']
    # V4.2: full-house ordinary package is A+. S-tier is promotion-only.
    if cs >= 2 and ss >= 2 and axs >= 2 and tw == 0:
        return 'A+', f'2核心✅ + 2半核心✅ + {axs}輔助✅ + 0❌ (全滿)'
    if cs >= 2 and tw == 0:
        return 'A', f'2核心✅ + 0❌'
    if (cs >= 1 and ss >= 1 and tw == 0) or (cs >= 2 and tw <= 1):
        return 'A-', f'{cs}核心✅ + {ss}半核心✅ + {tw}❌'
    if cs >= 1 and tw <= 1:
        return 'A-', f'{cs}核心✅ + ❌≤1'
    if (cs >= 1 and tw == 2) or (ss >= 2 and tw <= 1):
        return 'B+', f'{cs}核心✅/{ss}半核心✅ + {tw}❌'
    if ss >= 1 and axs >= 2 and tw <= 2:
        return 'B', f'{ss}半核心✅ + {axs}輔助✅ + {tw}❌'
    if cs == 0 and ss == 0 and axs >= 3 and tw <= 2:
        return 'B-', f'0核心/半核心✅ + {axs}輔助✅'
    if tw == 3 and (cs >= 1 or ss >= 1):
        return 'C+', f'{tw}❌ + 有核心/半核心✅挽救'
    if tw == 3:
        return 'C', f'{tw}❌ + 無核心/半核心✅'
    if tw == 4:
        return 'C-', f'{tw}❌'
    ts = c['total_strong']
    if tw >= 5 and (cs >= 1 or ss >= 1 or axs >= 2):
        return 'D+', f'{tw}❌ + 有✅({ts})'
    if tw >= 5 or ts == 0:
        return 'D', f'{tw}❌ / 無✅'
    return 'C', f'未匹配 (core✅={cs}, semi✅={ss}, aux✅={axs}, ❌={tw})'

def _grade_idx(g: str) -> int:
    return GRADE_ORDER.index(g) if g in GRADE_ORDER else 99

def _apply_core_constraint(grade: str, counts: dict, dims: dict) -> tuple:
    if not counts['has_core_weak']:
        return grade, ''
    # ❌❌ in core = strict cap at B+, no exemption
    if counts.get('has_core_double_weak'):
        if _grade_idx(grade) < _grade_idx('B+'):
            return 'B+', '核心防護牆: 核心❌❌ → 封頂B+ (無豁免)'
        return grade, ''
    if _grade_idx(grade) < _grade_idx('B+'):
        return 'B+', '核心防護牆: 核心❌ → 封頂B+'
    return grade, ''

def _apply_micro(grade: str, direction: str) -> tuple:
    i = _grade_idx(grade)
    if direction in ('UP', '+', '升', '升一級') and i > 0:
        return GRADE_ORDER[i - 1], '升一級'
    elif direction in ('DOWN', '-', '降', '降一級') and i < len(GRADE_ORDER) - 1:
        return GRADE_ORDER[i + 1], '降一級'
    return grade, '無變動'


def _is_debut_runner_hkjc(h_logic: dict) -> bool:
    if h_logic.get('debut_runner', False) or h_logic.get('is_debut', False):
        return True
    if str(h_logic.get('career_tag', '')).upper() == 'DEBUT':
        return True
    try:
        return int(h_logic.get('career_race_starts', h_logic.get('hk_starts', 999))) == 0
    except (TypeError, ValueError):
        return False


def _apply_debut_cap_hkjc(grade: str, h_logic: dict) -> tuple:
    """Debut runners keep normal matrix arithmetic but cannot exceed A."""
    if not _is_debut_runner_hkjc(h_logic):
        return grade, ''
    if _grade_idx(grade) < _grade_idx('A'):
        return 'A', f'初出馬封頂: 7維矩陣照計，但 final rating 最高A ({grade}→A)'
    return grade, '初出馬封頂檢查: 未超過A'

def _apply_double_strong_promotion(grade: str, counts: dict) -> tuple:
    """Promote A+ to S-tier only via core/semi-core ✅✅ conviction evidence."""
    if grade != 'A+':
        return grade, ''
    steps = min(counts.get('core_dbl_strong', 0), 2)
    if counts.get('semi_dbl_strong', 0) >= 2:
        steps += 1
    if steps <= 0:
        return grade, ''
    promo_map = {1: 'S-', 2: 'S', 3: 'S+'}
    final = promo_map.get(min(steps, 3), 'S+')
    return final, f'✅✅推進+{min(steps, 3)} → {final}'

