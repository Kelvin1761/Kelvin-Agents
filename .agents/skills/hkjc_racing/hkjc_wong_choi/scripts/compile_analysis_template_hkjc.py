def main():
    try:
        sys.stdout.reconfigure(encoding='utf-8', errors='replace')
    except Exception:
        pass
    parser = argparse.ArgumentParser()
    parser.add_argument("facts", help="Facts.md Path")
    parser.add_argument("logic_json", help="Logic.json Path")
    parser.add_argument("--output", required=True, help="Output Analysis.md Path")
    args = parser.parse_args()
    
    facts_text = Path(args.facts).read_text(encoding='utf-8')
    with open(args.logic_json, 'r', encoding='utf-8') as f:
        logic_data = json.load(f)
        
    # ── V4.2: Schema validation gate ──
    schema_result = validate_logic_json(logic_data)
    if not schema_result['pass']:
        print(f"🚨 V4.2 Schema validation FAILED ({len(schema_result['errors'])} errors):", file=sys.stderr)
        for err in schema_result['errors']:
            print(f"  ❌ {err}", file=sys.stderr)
        if os.path.exists(args.output):
            quarantine_file(args.output, f"V4.2 schema validation failed:\n" + "\n".join(schema_result['errors']))
        _write_compile_blocked(args.output, "\n".join(schema_result['errors']))
        sys.exit(1)
    for w in schema_result.get('warnings', []):
        print(f"  ⚠️ {w}")
    print(f"✅ V4.2 schema validation passed ({schema_result['stats']['horses_passed']}/{schema_result['stats']['horses_checked']} horses)")

    try:
        # Pre-compile JSON guard (dummy content)
        assert_no_dummy_json(logic_data, f"{Path(args.logic_json).name} before compile", allow_pending_fill=False)
        
        # Also assert core_logic is present and not empty
        for h_key, h_logic in logic_data.get('horses', {}).items():
            if not h_logic.get('core_logic'):
                raise ValueError(f"HKJC compile blocked: missing core_logic for horse {h_key} ({h_logic.get('horse_name','unknown')})")
            matrix_errors = validate_hkjc_matrix_confidence(h_logic)
            if matrix_errors:
                raise ValueError(
                    f"HKJC compile blocked: matrix confidence failed for horse {h_key} "
                    f"({h_logic.get('horse_name','unknown')}):\n"
                    + "\n".join(matrix_errors)
                )

    except ValueError as e:
        print(f"🚨 Pre-compile check failed:\n{e}", file=sys.stderr)
        _write_compile_blocked(args.output, str(e))
        if os.path.exists(args.output):
            quarantine_file(args.output, f"Pre-compile failed:\n{e}")
        sys.exit(1)
        
    facts_horses = gs.parse_facts_md_hkjc(facts_text)
    
    body = []
    body.append(build_hkjc_panorama_compiled(logic_data, facts_text, facts_horses))
    body.append('---\n#### [第二部分] 全場馬匹深度分析\n')
    
    horses_logic = logic_data.get('horses', {})

    # ── Compute grades and write back to JSON ──
    json_modified = False
    for h_key, h_logic in horses_logic.items():
        m_data = h_logic.get('matrix', {})
        try:
            grade_result = _compute_letter_grade(m_data, h_logic)
        except ValueError as e:
            print(f"🚨 Compile error:\n{e}", file=sys.stderr)
            _write_compile_blocked(args.output, str(e))
            if os.path.exists(args.output):
                quarantine_file(args.output, f"Compile error:\n{e}")
            sys.exit(1)
            
        h_logic['base_rating'] = grade_result['base_grade']
        h_logic['final_rating'] = grade_result['final_grade']
        json_modified = True

    for h in facts_horses:
        hl = horses_logic.get(str(h['num']), horses_logic.get(h['num'], {}))
        if not hl:
            hl = {"rating": "[未分析]", "core_logic": "[查無JSON節點]", "matrix": {}}
            
        body.append(generate_hkjc_horse_compiled(h, hl))
        body.append('---\n')
        
    body.append(build_hkjc_verdict_compiled(logic_data, facts_horses))
    
    final_markdown = '\n'.join(body)
    
    try:
        # Post-compile Markdown text guard
        assert_no_dummy_text(final_markdown, f"{Path(args.output).name} Analysis.md")
    except ValueError as e:
        print(f"🚨 Post-compile check failed:\n{e}", file=sys.stderr)
        
        # Write compile block reason
        blocked_file = Path(args.output).parent / ".runtime" / f"compile_blocked_{Path(args.output).stem}.txt"
        blocked_file.parent.mkdir(parents=True, exist_ok=True)
        blocked_file.write_text(str(e), encoding='utf-8')
        
        if os.path.exists(args.output):
            quarantine_file(args.output, f"Post-compile dummy text detected:\n{e}")
        sys.exit(1)
    
    # Atomic write: temp file → os.replace → final output
    # Never leaves a half-written Analysis.md on disk
    tmp_output = args.output + '.tmp'
    try:
        Path(tmp_output).write_text(final_markdown, encoding='utf-8')
        os.replace(tmp_output, args.output)
    except Exception:
        # Clean up partial temp file on any write error
        if os.path.exists(tmp_output):
            os.unlink(tmp_output)
        raise
    print(f"✅ HKJC Analysis 組裝完成 → {args.output}")

    # ── Write computed grades back to Logic.json ──
    if json_modified:
        with open(args.logic_json, 'w', encoding='utf-8') as f:
            json.dump(logic_data, f, ensure_ascii=False, indent=2)
        print(f"✅ 已回寫計算評級至 → {Path(args.logic_json).name}")

if __name__ == '__main__':
    main()
