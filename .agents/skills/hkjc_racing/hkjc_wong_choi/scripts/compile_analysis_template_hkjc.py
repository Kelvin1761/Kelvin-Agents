def _compute_letter_grade(m_data: dict, h_logic: dict) -> dict:
    """Compute the proper ABCD letter grade from matrix numeric scores.
    Falls back to final_rating-based inference when matrix is absent.
    """
    matrix_missing = not m_data  # no matrix at all

    if matrix_missing:
        h_name = h_logic.get('horse_name', 'unknown')
        raise ValueError(
            f"HKJC compile blocked: matrix missing for {h_name}. "
            "Do not infer matrix from final_rating."
        )

    # Check for missing dimensions
    required_dims = {
        "stability", "sectional", "race_shape", "trainer_signal",
        "horse_health", "form_line", "class_advantage"
    }
    missing = required_dims - set(m_data.keys())
    if missing:
        h_name = h_logic.get('horse_name', 'unknown')
        raise ValueError(f"HKJC compile blocked: missing matrix dimensions {sorted(missing)} for {h_name}")

    # Step 1: Convert numeric scores to ✅/❌/➖
    dims = {}
    for json_key, dim_key in MATRIX_TO_DIM.items():
        item = m_data.get(json_key, {})
        score = item.get('score', 5)
        dims[dim_key] = _score_to_tick(score)

    # Step 2: Count dimensions
    counts = _count_dimensions(dims)

    # Step 3: Check forgiveness bonus (5-tier aware)
    forg_item = m_data.get('forgiveness_bonus', {})
    forg_tick = _score_to_tick(forg_item.get('score', 0))
    if forg_tick == '✅✅':
        counts['aux_strong'] += 1
        counts['total_strong'] += 1
        counts['aux_dbl_strong'] += 1
        counts['total_dbl_strong'] += 1
    elif forg_tick == '✅':
        counts['aux_strong'] += 1
        counts['total_strong'] += 1

    # Step 4: Lookup base grade
    base_grade, base_rule = _lookup_base_grade(counts)

    # Step 5: Core constraint
    constrained_grade, constraint_note = _apply_core_constraint(base_grade, counts, dims)

    # Step 7: Micro adjustment
    ft = h_logic.get('fine_tune', {})
    ft_dir = ft.get('direction', '無') if isinstance(ft, dict) else str(ft)
    
    # Skip micro adjustment when matrix is missing (grade already exact)
    adjusted_grade, micro_note = _apply_micro(constrained_grade, ft_dir.upper())
    promoted_grade, promo_note = _apply_double_strong_promotion(adjusted_grade, counts)
    if promo_note:
        adjusted_grade = promoted_grade
        micro_note = f'{micro_note}; {promo_note}'

    capped_grade, debut_note = _apply_debut_cap_hkjc(adjusted_grade, h_logic)
    if debut_note:
        adjusted_grade = capped_grade
        micro_note = f'{micro_note}; {debut_note}'
    capped_grade, cap_notes = apply_hkjc_matrix_caps(adjusted_grade, h_logic)
    if cap_notes:
        adjusted_grade = capped_grade
        micro_note = f"{micro_note}; {'; '.join(cap_notes)}"

    return {
        'dims': dims,
        'counts': counts,
        'base_grade': base_grade,
        'base_rule': base_rule,
        'constraint_note': constraint_note,
        'adjusted_grade': adjusted_grade,
        'micro_note': micro_note,
        'final_grade': adjusted_grade,
        'matrix_missing': matrix_missing,
        'cap_notes': cap_notes if 'cap_notes' in locals() else [],
    }

